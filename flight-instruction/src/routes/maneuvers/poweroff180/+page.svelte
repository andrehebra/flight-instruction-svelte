<Heading tag='h1'>Power-Off 180</Heading>
<P>The power-off 180 is a great maneuver that showcases your ability to control the airplane with no power and land it on a specific spot on the runway. The nature of the maneuver, however, leads to many people performing it in their own way.</P>
<div class="image"><Img size="max-w-full" src=https://i.imgur.com/HchHKUm.png></Img></div>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>The Purpose</Heading>
<P>Power-Off 180s are designed to ensure that you have the proper skills necessary to pick a landing spot and land there by gliding. For the maneuver, you will be required to land within 200 feet beyond your designated spot on the runway. The point isn't to land right at the beginning of a long runway during an actual emergency, so remember that if you do have an engine failure in the pattern it is better to use your judgement and pick a touchdown spot as appropriate. The skills you accumulate from this maneuver will help you immensely.</P>
<Heading tag='h2'>Performing the maneuver</Heading>
<P>Enter just like you are doing a normal traffic pattern. Remember that if you are at a towered airport you need to request a short approach. If at an untowered airport be sure not to cut off any traffic that could be on final.</P>
<P>Pick you landing spot. It is a good idea to consistently use the same one to become more consistent at performing the maneuver. A good spot would be the beginning of the 500' markers. Consistently using the same spot will also allow you to know where 200 feet beyond the point is.</P>
<Heading tag='h5'>Entering</Heading>
<P>When you are abeam your landing spot, pull the power to idle and maintain your pitch for a few seconds to bleed off airspeed. While you may think to pitch for best glide, in reality, with a normal traffic pattern, you will have no problem reaching your point and don't need to let your speed go so low.</P>
<P>Remember, we want this to resemble a normal pattern landing as much as possible, and one way to achieve this is to fly at normal speeds with shorter legs to account for the loss of engine power.</P>
<Heading tag='h5'>Turning Base</Heading>
<P>To maintain a proper glidepath, instead of starting your base turn when your aiming point is 45 degrees behind you, begin when it is 30 degrees behind you. At this point, however, it becomes more difficult to give specific instructions.</P>
<P>In the base turn, you will have to decide whether to extend your flaps further or keep them in. There isn't an exact metric you can use to judge this, it just takes practice.</P>
<Heading tag='h5'>The Rest</Heading>
<P>Fly as close to a normal pattern as you can while making adjustments to get on your proper spot. This is going to involve a lot of on-the-fly decision making. There are, however, a few tips that you should keep in mind.</P>
<Heading tag='h2'>Tips to Get Your Spot</Heading>
<Heading tag='h5'>Be Careful With Flap Extensions</Heading>
<P>Flaps can definitely be helpful in losing extra altitude when you need it, and should be used accordingly. Just remember that going from flaps 20 to 30 will cause a much higher sink rate compared to putting out 10 degrees. Reserve fully extending the flaps until you know you can make your point with the extra sink rate.</P>
<Heading tag='h5'>Flaps on Roundout</Heading>
<P>If you're coming in short, flaps can actually help you extend your roundout and flare. In the roundout, if you don't think you will make your landing spot, you can dump the flaps and use the increase in lift to hold the airplane in ground effect as long as possible to hopefully make it to your spot.</P>
<Heading tag='h5'>Adjust your Base Turn</Heading>
<P>We're not looking for a perfect rectangle pattern here. If you're too low, start heading more towards the runway. If you're too high, turn away from the runway to increase your ground track. Just make sure to make small adjustments.</P>
<Heading tag='h5'>Forward Slip</Heading>
<P>This is a perfect example of a time to execute a forward slip. It's a great way to lose altitude quickly without increasing your airspeed and causing you to float down the runway. You learned it in private for a reason, you might as well use it!</P>
