<Heading tag='h1'>Short Field Takeoff and Maximum Performance Climb</Heading>
<P>Short-Field takeoffs are incredibly useful useful maneuver for whenever you need to get the maximum performance out of the airplane during the takeoff roll and initial climb out. It's important, however, to properly understand how to do the maneuver and the limitations that they have.</P>
<Heading tag='h2'>Completion Standards</Heading>
<P>The private and commercial ACS include a section for the "Short-Field Takeoff and Maximum Performance Climb" with nearly the same completion standards, the only difference being tighter tolerances for airspeed in the commercial ACS, which is shown below.</P>
<Heading tag='h5'>Objective</Heading>
<P>To determine that the applicant exhibits satisfactory knowledge, risk management, and skills associated with a short-field takeoff, maximum performance climb operations, and rejected takeoff procedures.</P>
<Heading tag='h5'>Knowledge</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Effects of atmospheric conditions, including wind, on takeoff and climb performance</Li>
<Li>Vx and Vy</Li>
<Li>Appropriate airplane configuration</Li></List>
<Heading tag='h5'>Risk Management</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Selection of runway based on pilot capability, airplane performance and limitations, available distance, and wind</Li>
<Li>Effects of crosswind, windshear, tailwind, wake turbulence, runway surface/condition</Li>
<Li>Abnormal operations, to include planning for: rejected takeoff and engine failure in takeoff/climb phase of flight</Li>
<Li>Collision hazards, to include aircraft, terrain, obstacles, wires, vehicles, persons, and wildlife</Li>
<Li>Low altitude maneuvering including stall, spin, or CFIT</Li>
<Li>Distractions, loss of situational awareness, or improper task management</Li></List>
<Heading tag='h5'>Skills</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Complete the appropriate checklist</Li>
<Li>Make radio calls as appropriate</Li>
<Li>Verify assigned/correct runway</Li>
<Li>Ascertain wind direction with or without visible wind direction indicators</Li>
<Li>Position the flight controls for the existing wind</Li>
<Li>Clear the area, taxi into takeoff position and align the airplane on the runway centerline utilizing maximum available takeoff area</Li>
<Li>Apply brakes while setting engine power to achieve maximum performance</Li>
<Li>Confirm takeoff power prior to brake release and verify proper engine and flight instrument indications prior to rotation</Li>
<Li>Rotate and lift off at the recommended airspeed and accelerate to the recommended obstacle clearance airspeed or VX, +10/-5 knots (private) or VX ±5 knots (commercial)</Li>
<Li>Establish a pitch attitude that will maintain the recommended obstacle clearance airspeed or VX, +10/-5 knots (private) or ±5 knots (commercial) until clearing the obstacle or until the airplane is 50 feet above the surface</Li>
<Li>Establish a pitch attitude for VY and accelerate to VY +10/-5 knots (private) or ±5 knots (commercial) after clearing the obstacle or at 50 feet AGL if simulating an obstacle</Li>
<Li>Configure the airplane in accordance with the manufacturer’s guidance after a positive rate of climb has been verified</Li>
<Li>Maintain VY +10/-5 knots (private) or ±5 knots (commercial) to a safe maneuvering altitude</Li>
<Li>Maintain directional control and proper wind-drift correction throughout takeoff and climb</Li>
<Li>Comply with noise abatement procedures</Li></List>
<Heading tag='h2'>Calculating Takeoff Distances</Heading>
