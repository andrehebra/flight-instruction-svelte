<P>Takeoff distances in the 172S are only given for the short field configuration, and include figures for the ground roll and the total distance to clear a 50 foot tall obstacle. These calculations must take into account the pressure altitude, temperature, wind, runway surface, and weight. Also, remember that all of these figures were calculated using ideal conditions and a test pilot who was trained specifically for this task, so you need to add a little bit of margin to the figures.</P>
<Heading tag='h2'>When to Use the Short-Field Takeoff</Heading>
<P>Any time there is a consideration of a short runway or obstacles off the departure end of the runway is a perfect time to do a short-field takeoff. However, careful consideration needs to be made to ensure that you are within safe margins of safety.</P>
<Heading tag='h2'>The Procedure</Heading>
<Heading tag='h5'>Configuration</Heading>
<P>Short-field takeoffs should be done with 10 degrees of flaps in the 172S. Double check the flaps are set correctly before entering the runway, and when completing the line up checklist.</P>
<Heading tag='h5'>Use ALL of the Runway</Heading>
<P>Runway behind you is always wasted. When you line up, make sure that you are giving yourself the maximum amount of distance available down the runway as reasonably possible. Square off your turn to line up with the runway so that you aren't taxiing forward on the runway before starting the takeoff roll.</P>
<Heading tag='h5'>Applying Full Power</Heading>
<P>First, make sure that you are firmly and equally applying brakes to ensure that when adding power the airplane does not move forward. Then smoothly apply full power and check that the RPMs are between 2300 and 2400, call out "Engine Instruments Normal."</P>
<Heading tag='h5'>The Takeoff Roll</Heading>
<P>Once the engine instruments are checked, release the brakes. Make sure to mainly control direction on the ground with rudder and not the brakes. Other than that, the first part of the takeoff roll until rotation will be very similar to a normal takeoff. Instead of allowing the nose wheel to rise, however, make sure to hold it on the ground with the elevator until rotation speed (55 kts) is reached.</P>
<Heading tag='h5'>Lift Off and Initial Climb</Heading>
<P>Once you have reached rotation speed, pitch up to maintain the Obstacle Barrier Speed (56 kts). This will look like a slightly higher Vx pitch. Maintain this pitch and airspeed until you have cleared your selected obstacle. Once clear, establish a Vx pitch and retract the flaps. Slowly transition to a Vy pitch once the flaps are retracted and continue a normal climb.</P>
<Heading tag='h2'>Common Errors</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Failure to review AFM/POH and performance charts prior to takeoff</Li>
<Li>Failure to adequately clear the area</Li>
<Li>Failure to utilize all available runway/takeoff area</Li>
<Li>Failure to have the airplane properly trimmed prior to takeoff</Li>
<Li>Premature lift-off resulting in high drag</Li>
<Li>Holding the airplane on the ground unnecessarily with excessive forward-elevator pressure</Li>
<Li>Inadequate rotation resulting in excessive speed after lift-off</Li>
<Li>Inability to attain/maintain VX</Li>
<Li>Fixation on the airspeed indicator during initial climb</Li>
<Li>Premature retraction of landing gear and/or wing flaps</Li></List>
