<Heading tag='h2'>Common Errors</Heading>
<P></P>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Failure to adequately clear the area before taxiing into position on the active runway</Li>
<Li>Insufficient back-elevator pressure during the initial takeoff roll, resulting in an inadequate angle of attack</Li>
<Li>Failure to cross-check engine instruments for indicators of proper operation after applying power</Li>
<Li>Poor directional control</Li>
<Li>Allowing the airplane to pitch up excessively, causing a tail strike</Li>
<Li>Climbing too steeply after lift-off</Li>
<Li>Abrupt and/or excessive elevator control while attempting to level off and accelerate after lift-off</Li>
<Li>Allowing the airplane to "mush" or settle, resulting in an inadvertent touchdown after lift-off</Li>
<Li>Attempting to climb out of ground effect area before attaining sufficient climb speed</Li>
<Li>Failure to anticipate an increase in pitch attitude as the airplane climbs out of ground effect</Li>
<Li>Inadequate drift correction after lift-off</Li></List>
