<Heading tag='h1'>Soft-Field Takeoff</Heading>
<P></P>
<P>Soft-field takeoffs not only expand your options of where to fly, but they are also a great exercise that will expand your skills as a pilot. Training your skills in precise control of the airplane will make you a better pilot overall.</P>
<P></P>
<Heading tag='h2'>What is a Soft-Field Takeoff?</Heading>
<P></P>
<P>A soft-field takeoff is a technique used by pilots to safely depart from soft landing surfaces such as grass, gravel, or dirt runways. The goal is to keep as much weight off of the landing gear, specifically the nose wheel, as possible to reduce the risk of the wheels digging into the ground and potentially getting stuck.</P>
<P></P>
<Heading tag='h2'>Proper Technique</Heading>
<P></P>
<P>Everything pilots do in a soft-field takeoff, in comparison to a normal takeoff, is to reduce pressure on the landing gear. This is achieved by changing a few things, as shown below.</P>
<P></P>
<Heading tag='h5'>Flaps</Heading>
<P></P>
<P>Set the flaps to 10 degrees. We do this to provide as much lift as quickly as possible during takeoff. Double check this before entering the runway and when lining up.</P>
<P></P>
<Heading tag='h5'>Entering the Runway</Heading>
<P></P>
<P>To enter the runway while minimizing the risk of getting stuck, use minimal braking while applying full aft elevator. Unnecessary braking could cause one of the main landing gears to get stuck in the ground; but also could cause added forces on the nose wheel since braking will cause a nose down force.</P>
<P></P>
<Heading tag='h5'>Don't Stop on the Runway</Heading>
<P></P>
<P>Once you are lined up and have confirmed that you are on the correct runway, smoothly apply full power. Because you have maintained full aft elevator, this will cause the nose to rise up relatively quickly. Counteract this by removing some aft elevator as the nose rises to the appropriate position.</P>
<P></P>
<Heading tag='h5'>The Correct Sight Picture</Heading>
<P></P>
<P>During the ground roll, we want to remove the pressure off of the nose wheel, and we achieve that with a small amount of aft elevator to keep the nose wheel off of the ground. We aren't looking for any kind of aggressive pitch attitude here, especially since it would be relatively easy to cause a tail strike. Look for a pitch attitude that is less than a normal flare on landing.</P>
<P></P>
<Heading tag='h5'>The Takeoff Roll</Heading>
<P></P>
<P>Besides the back pressure on the elevator, everything else about the takeoff until rotation should be like a normal takeoff. Slowly remove crosswind corrections as the controls become more effective, and call out that the airspeed is alive and climbing. Check that the engine instruments are normal and make the "Engine Instruments Normal" call out.</P>
<P></P>
<Heading tag='h5'>Rotation</Heading>
<P></P>
<P>Given our aggressive pitch attitude, the airplane is going to rotate at a slower airspeed compared to normal. This is beneficial as it allows us to completely remove any risk of a nose wheel digging into the ground at a high ground speed. Because of the low speed, however, we want to quickly add some forward pressure on the elevator to remain in ground effect. Because we are at a very low airspeed, we might not be able to fly out of ground effect, meaning that going too high before attaining the proper speed could cause a stall.</P>
<P></P>
<Heading tag='h5'>Crosswind Corrections in Ground Effect</Heading>
<P></P>
<P>Once in ground effect, use a slip to remain on the centerline while properly aligned with the runway. Doing this will reduce the chances of side loading the airplane if for any reason we touch back down on the runway.</P>
<P></P>
<Heading tag='h5'>Initial Climb (No Obstacle)</Heading>
<P></P>
<P>Once reaching 55kts, establish a Vy pitch attitude and retract the flaps as the airspeed increases to 74 kts. Then climb out just as you would with a normal takeoff.</P>
<P></P>
<Heading tag='h5'>Initial Climb (Obstacle)</Heading>
<P></P>
<P>If you are simulating an obstacle, pitch for the obstacle barrier speed of 56kts, similar to a short field takeoff. Once clear, pitch for Vx (62 kts) and retract the flaps. Once the flaps are at 0 degrees, pitch for Vy and resume a normal climb.</P>
