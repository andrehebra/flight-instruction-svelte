<Heading tag='h1'>Revision</Heading>
<div class="image"><Img size="max-w-full" src=https://i.imgur.com/pvkU0Qe.jpeg></Img></div>
<P>Revision is critical to improving any piece of work. Throughout this semester, we focused on a variety of revision strategies that allowed us to continually improve each draft of our projects. Additionally, we gained an understanding of the importance of revision and just writing something, rather than nothing.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>QQC #1</Heading>
<P>Creating something, whether textual or not, requires starting. No sculptor accidentally created a beautiful sculpture, and the same goes for all of the writing that we do. QQC #1 focused on this fact, with the wonderfully titled article "Shitty First Drafts"</P>
<Button href='https://wrd.as.uky.edu/sites/default/files/1-Shitty%20First%20Drafts.pdf'> Anne Lamott - Shitty First Drafts</Button>
<P>The assignment required us to come up with a question in response to the article and respond to a quote that we found interesting.</P>
<P>Question (me): </P>
<Blockquote border bg class="p-4 my-4"><P size="md" height="relaxed">What are some methods of becoming more loose and open when writing shitty first drafts? Even though I know no one is going to read it, it's hard for me to just let the words start coming out.</P></Blockquote>
<P>Quote from article:</P>
<Blockquote border bg class="p-4 my-4"><P size="md" height="relaxed">"It was almost just typing, just making my fingers move. And the writing would be terrible."</P></Blockquote>
<P>Response to quote (me):</P>
<Blockquote border bg class="p-4 my-4"><P size="md" height="relaxed">This quote really caught my attention, I've never been someone who is gifted at writing and had the notion that a lot of other people just sat down and wrote beautiful prose. I think that has probably caused me to avoid advancing my writing skills much further than necessary for my career.</P></Blockquote>
<P>While revision is oftentimes seen as fixing work, the first step in getting to that point is actually creating the work that requires fixing. This assignment aided in understanding that nobody's first draft of anything is going to be a work of art, but through the process of revision, it can become one.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Peer Review</Heading>
<P>The peer review assignments for both projects 1 and 2 were extremely helpful in understanding the dynamic process that revision takes. First of all, seeing the mistakes that everyone makes allows for less self-judgment. Secondly, being able to send and receive constructive and positive feedback allows for major improvement, as a fresh set of eyes can see things that a writer can not.</P>
<P>My feedback for the Project 2 peer review is shown below:</P>
<Blockquote border bg class="p-4 my-4"><P size="md" height="relaxed">This was a great and captivating read. Not too many things that I think you should change<br><br>1. I like the structure of the essay. I think that it flows relatively well between the first and second artifact, and the analysis of the tone used in each was awesome.<br>2. One note, you explained logos in the beginning of the essay and also at the end, so I think you could remove the second explanation.<br>3. I think a good way that you could connect the artifacts together more would be by having a longer conclusion that specifically focuses on the connections between the two. I already liked the individual analysis and I think adding more to the end would be more beneficial than taking away from either of the individual analyses.<br>4. Overall, it was very good, especially as a draft. The intro was very captivating and made me more interested in the topic.</P></Blockquote>
<P>Overall, reading and responding to other people's papers was not only useful in getting direct feedback from others, but also analyzing the writing styles of others allowed me to improve my own work. I found that responding to the work of others inspired me to make similar improvements in my own work.</P>
