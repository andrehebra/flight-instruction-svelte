<P>Remember that almost anything that has an effect on your physical or mental condition can degrade your vision.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Environmental Conditions that Degrade Vision</Heading>
<P>There are many different environmental conditions that can degrade you ability to see well.</P>
<Heading tag='h5'>Darkness</Heading>
<P>It can take 30 minutes for your eyes to adjust to darkness. Unfortunately, however, you can quickly lose your night vision by looking at a bright light for a few seconds.</P>
<P>Tips for maintaining night vision:</P>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Close one eye when you need to look at something bright to maintain your night vision</Li>
<Li>Dim cabin lights and displays</Li>
<Li>Use red lights as to not interfere with night vision</Li></List>
<Heading tag='h5'>High Altitude</Heading>
<P>Flying at higher altitudes can impair your vision, especially at night.</P>
<P>Besides what the legal requirements are, it is recommended that pilots use supplemental oxygen at pressure altitudes above 10,000 feet MSL during the daytime and 5,000 MSL at night.</P>
<Hr class="my-8" height="h-px" />
