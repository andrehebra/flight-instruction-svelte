<Heading tag='h5'>Visual Blind Spot</Heading>
<P>The area where the optic nerve connects to the retina in the back of each eye is known as the optic disk. In that area, there is a total absence of cones and rods, and therefore you are not able to see anything in that spot.</P>
<P>Focus on the airplane in the image below. Cover or close your left eye and move towards the image. You will eventually notice where the X on the right side disappears.</P>
