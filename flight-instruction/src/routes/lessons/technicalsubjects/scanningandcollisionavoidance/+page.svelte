<P>Your blind spot is normally subconsciously tuned out by your brain, but you need to consider it as a risk in being able to avoid other traffic.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>See and Avoid</Heading>
<P>All pilot's must maintain vigilance at all times while operating an aircraft, both on the ground and in the air.</P>
<P>Don't ever let ATC give you a false sense of security when it comes to other traffic. There is always the potential of a collision, and you can dramatically reduce that risk by staying focused.</P>
<P>If you see other traffic, regardless of whether or not you have the right of way, make sure to stay clear of it. It is always possible that other traffic does not see you.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Proper Visual Scanning</Heading>
<P>Pilot's need to continually scan for traffic throughout their flight.</P>
<P>A good scan will go from one side to the other, and stop in increments of about 10 degrees. Focus for a second or two on that specific area, and then move on to the next area.</P>
<P>It can be extremely difficult to notice other traffic when you are not making a conscious effort or focusing in a specific area.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Proper Clearing Procedures</Heading>
<P>You should always make sure that an area is clear of traffic before you enter it. Here are a few tips for different operations:</P>
<Heading tag='h5'>Before Takeoff</Heading>
<P>Angle the airplane towards the approach path when holding short of a runway before takeoff. This will allow you to check if traffic is on final approach before you take the runway.</P>
<P>Scan the entire approach area and runway to make sure there is no potential for a collision.</P>
<Heading tag='h5'>Climbs and Descents</Heading>
<P>Use gentle banks from one side to another to allow you to scan above or below the airplane.</P>
<Heading tag='h5'>Straight and Level</Heading>
<P>Constantly scan for traffic with proper visual scanning technique.</P>
<Heading tag='h5'>Before Maneuvers</Heading>
<P>Before any maneuver, make sure to execute clearing turns. This should include either two 90 degree turns or a 180 degree turn.</P>
<P>Always clear in the direction that you will be doing the maneuver in. For example, before a steep turn to the left, turn 90 degrees to the left and 90 degrees back to make sure you will not interfere with any other traffic.</P>
<Heading tag='h5'>Traffic Patterns</Heading>
<P>Always scan for traffic when entering the traffic pattern and when inside.</P>
<P>Before turning base or final, lift the wingtip (in a high wing airplane) and look for traffic on final.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Airplane Blind Spots</Heading>
<P>High wing airplanes have blind spots above them, while low wing airplanes have blind spots below due to the wings.</P>
<P>Before turns in a high wing, lift the wingtip and look for traffic in the direction of the turn.</P>
<P>Before turns in a low wing, lower the wingtip and look for traffic in the direction of the turn.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Speed Differential and Collision Risk</Heading>
<P>An increased speed differential will result in pilot's having less time to react and avoid a collision.</P>
<P>Additionally, head on collisions can be difficult to notice as it will not appear as though the aircraft is moving along the windshield.</P>
