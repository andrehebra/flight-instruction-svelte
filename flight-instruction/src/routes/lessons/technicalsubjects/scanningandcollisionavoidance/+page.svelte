<Heading tag='h1'>Visual Scanning and Collision Avoidance</Heading>
<P>The majority of aircraft collisions happen during the daytime in clear weather. Oftentimes this gives pilots a sense of security, but it is incredibly important that pilots maintain vigilance in all different conditions.</P>
<P>Risk factors for a collision include:</P>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Below 3,000 feet AGL</Li>
<Li>Within 5 miles of an airport</Li>
<Li>During daytime clear weather</Li>
<Li>Aircraft blind spots (low and high wing)</Li></List>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Vision Related to Current Physical Condition</Heading>
<P>Nobody has perfect vision, but there are things that can affect a person's ability to see well.</P>
<P>Things that have an impact on a pilot's vision:</P>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Diet</Li>
<Li>Physical Health</Li>
<Li>Illness</Li>
<Li>Medication</Li>
<Li>Stress</Li>
<Li>Alcohol</Li>
<Li>Fatigue</Li>
<Li>Emotion</Li>
<Li>Hypoxia</Li></List>
