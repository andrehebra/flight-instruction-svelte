<Heading tag='h5'>Don'ts</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Ridicule the learner's performance</Li>
<Li>Use profanity</Li>
<Li>Model irresponsible flight behaviors</Li>
<Li>Say one thing but do another</Li>
<Li>Forget personal hygiene</Li>
<Li>Disrespect the learner</Li>
<Li>Demand unreasonable progress</Li>
<Li>Forget the learner is new to aviation jargon</Li>
<Li>Set the learner up for failure</Li>
<Li>Correct errors without an explanation of what went wrong</Li></List>
<Heading tag='h5'>Acceptance of the Learner</Heading>
<P>An instructor needs to accept learners as they are, including their faults and problems. The learner wants to learn, and the instructor needs to help them along in that process. Make sure that both the instructor and learner are working towards the same objective, and under no circumstance do anything which implies degrading the learner. Always treat the learner with respect, whether or not they are a fast learner.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Evaluation of Student Ability</Heading>
<P>Evaluation refers to judging a learner's ability to perform a maneuver or procedure.</P>
<Heading tag='h5'>Demonstrated Ability</Heading>
<P>Evaluation of demonstrated ability is based on established standards of performance modified to apply to the learner's experience and stage of development as a pilot. The evaluation needs to consider the learner's mastery of the elements involved in the maneuver or procedure, rather than just the overall performance.</P>
<Heading tag='h5'>Keeping the Learner Informed</Heading>
<P>When evaluating, the instructor needs to keep the learner informed of progress. This can be done as each maneuver is completed, or summarized during a postflight or class critique. These should be in a written format. When critiquing, make sure to explain proper corrections and how the learner can improve.</P>
