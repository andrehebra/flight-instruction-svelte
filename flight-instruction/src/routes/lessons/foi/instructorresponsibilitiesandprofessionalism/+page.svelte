<Heading tag='h2'>Instructor Professionalism</Heading>
<Heading tag='h5'>Do's</Heading>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Be professional at all times</Li>
<Li>Be sincere</Li>
<Li>Present a professional appearance and personal habits</Li>
<Li>Maintain a calm demeanor</Li>
<Li>Practice safety and accident prevention at all times</Li>
<Li>Avoid profanity</Li>
