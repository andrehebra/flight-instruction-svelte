<Heading tag='h1'>Maintaining Motivation</Heading>
<P>Helping foster a motivated attitude is the responsibility of a flight instructor. Unfortunately, there is not one way to motivate all learners using one strategy, so make sure to incorporate the following methods on a case-by-case basis.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Reward Success</Heading>
<P>Positive feedback encourages all learners. They want to make sure that the work they have put in is being noticed and makes a difference. Make sure, however, that your feedback is honest. Give positive feedback in the form of:</P>
<List ulClass="max-w" tag="ul" class="space-y-1"><Li>Praise incremental successes</Li>
<Li>Relate daily accomplishments to lesson objectives</Li>
<Li>Comment favorably on learner progress and level ability</Li></List>
<P>Additionally, even when a learner has a sub-par performance, make sure to give positive feedback on the things they did correctly. This should also be tailored to their current experience level.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Give Learners a Sense of Control</Heading>
<P>Allow learners some choice in the method of instruction that is being used. One of the best ways to keep people engaged is by giving them some choices regarding what happens in the classroom. Some learners will prefer a specific type of instruction, and they will be grateful and motivated when an instructor is considerate of their desires. </P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Be Clear About Learning Objectives</Heading>
<P>People naturally want to do well in relation to their goals, so make sure to be clear about what the expectations are of their performance. A goal cannot be achieved if there are not definable standards attached to it.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Create a Threat-Free Environment</Heading>
<P>Make sure to maintain a positive and non-threatening environment. Going back to human needs, a learner can not perform well or maintain motivation when their sense of security is in question. Especially in aviation, where many people may have a fear of flying, it is imperative to make sure that all learners feel comfortable during instruction.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Harness Learner Interests</Heading>
<P>Relate specific lessons to a learners interest, especially the ones that they might not find as interesting. Some lessons are not as fun or exciting as others, so make sure that you can relate the topic to something learners find interesting. Make sure that they see the light at the end of the tunnel, so to speak.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Make Things Fun</Heading>
<P>This one may seem obvious, but never forget that you're showing someone how to fly an airplane! It should be a fun, while challenging, experience. Not everything needs to be a game, but if a learner is having fun while learning, they are going to maintain or grow their motivation while also learning.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Provide Effective Feedback</Heading>
<P>Firstly, make it clear that nobody is going to be perfect at everything. When a learner under-performs, make sure that you are providing feedback that is useful in improving that performance.</P>
<Hr class="my-8" height="h-px" />
<Heading tag='h2'>Study Motivation</Heading>
<P>Make sure to research the theories of motivation. The Aviation Instructors handbook doesn't go too in-depth on the topic. The following articles have good explanations of how motivation works:</P>
<Button href='https://www.verywellmind.com/what-is-motivation-2795378'>Motivation: The Driving Force Behind Our Actions</Button>
<Button href='https://jamesclear.com/motivation'>Motivation: The Scientific Guide on How to Get and Stay Motivated</Button>
<P>These articles are not specific to instruction or aviation, however understanding what generally motivates people is a great way to become a better instructor.</P>
